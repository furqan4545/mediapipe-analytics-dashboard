// Helper to get the right icon component
export function PlatformIcon({ platform, className, size = "md" }: { platform: string } & IconProps) {
  switch (platform) {
    case "tiktok":
      return <TikTokIcon className={className} size={size} />
    case "instagram":
      return <InstagramIcon className={className} size={size} />
    case "youtube":
      return <YouTubeIcon className={className} size={size} />
    default:
      return null
  }
}

// For filter dropdowns - includes label
export function PlatformLabel({ platform }: { platform: string }) {
  const labels: Record<string, string> = {
    tiktok: "TikTok",
    instagram: "Instagram",
    youtube: "YouTube",
    all: "All Platforms",
  }

  return (
    <span className="flex items-center gap-2">
      {platform !== "all" && <PlatformIcon platform={platform} size="sm" />}
      <span>{labels[platform] || platform}</span>
    </span>
  )
}
